# Functions can be seen as variables, or objects
# Thus, by implication, variable naming rules apply to them as well
# A function can be assigned to a variable
# In this case, the variable then acts as an 'alias' for the function

# Let's assume I am too lazy to type 'print' in full all the time ;)
p = print # assigns print to the variable p

# Now we can use p as a normal function
# It is now an alias for 'print'
p("Hello world") # prints 'Hello world'
