# Define a function, my_func
# Functions are defined using the 'def' keyword
# It takes no input and returns no data
# All it does is print "Hello"
def my_func():
    print("Hello!")

# We can then call the function anywhere, as many times as we want
# We call it three times. Hence, "Hello" will be printed thrice
my_func()
my_func()
my_func()
