x = float(input("Enter number 1: ")) # Get first number
y = float(input("Enter number 2: ")) # Get second number

# Prints out values
print("{0} + {1} = {2}".format(x, y, x + y))
print("{0} - {1} = {2}".format(x, y, x - y))
print("{0} * {1} = {2}".format(x, y, x * y))
print("{0} / {1} = {2}".format(x, y, x / y))
print("{0} ** {1} = {2}".format(x, y, x ** y))
print("{0} // {1} = {2}".format(x, y, x // y))
print("{0} % {1} = {2}".format(x, y, x % y))
